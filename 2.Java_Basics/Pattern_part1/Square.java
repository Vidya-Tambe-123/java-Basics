package Pattern_part1;
public class Square{
    public static void main(String args[]){
        int n=4;//total no of rows
        int m=4;//total no of colums
        for(int i=1;i<=n;i++){
            for(int j=1;j<=m;j++){
                System.out.print("*");
            }
            System.out.println("");
        }

    }
    
}
