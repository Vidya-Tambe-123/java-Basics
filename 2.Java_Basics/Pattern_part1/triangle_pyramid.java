package Pattern_part1;

public class triangle_pyramid {
    public class tringle_star {
        public static void main(String []main){
            int n=4;
            for(int i=1;i<=n;i++){
                for(int j=1;j<i;j++){
                    System.out.print("*");
                }
                System.out.println("");
            }
        }
        
    }
    
    
}
