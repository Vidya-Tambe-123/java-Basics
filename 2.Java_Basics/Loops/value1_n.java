package Loops;

import java.util.*;

public class value1_n {
    public static void main(String args){
    Scanner sc=new Scanner(System.in);
       int n=sc.nextInt();
       int i=0 ;
       while(i<n){
        i++;
        System.out.println(i);
       }
       
       
       sc.close();
    }    
    
}
