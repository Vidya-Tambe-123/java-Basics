package Operators;

public class Unnary_Operator {
    public static void main(String args[]){
        int a=10;
        a++;
        System.out.println(a); 
        a--;
        System.out.println(a);
        a+=5;
        System.out.println(a);
        a-=5;
        System.out.println(a);
        a*=2;
        System.out.println(a);
        a/=2;
        System.out.println(a);
        a%=2;
        System.out.println(a);

    }
    
}
