package Pattern_part1;

public class rectangular_num {
    public class rectangular_no {
        public static void main(String args[]){
            int n=6;//no of rows
            int m=3;//no of col
            int num=1;//increment
            for(int i=1;i<=n;i++){
                for(int j=1;j<=m;j++){
                    System.out.print(num);
                    num++;
                }
                System.out.println(" ");
            }
        }
        
    }
    
    
}
